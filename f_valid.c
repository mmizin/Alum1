#include "libft.h"

int		f_valid(char *str)
{
	if (!*str)
	{
		return (0);
	}
	while (*str)
	{
		if (*str < '0' || *str > '9')
		{
			return (0);
		}
		else
			str++;
	}
	return (1);
}
